    public void SaveSubject(){
        SubjectObject SaveSubject = new SubjectObject();
        // Declare Getters
        String SubjectName = JtxtSubjectName.getText();
        int semester = cmbSemester.getSelectedIndex();
        String schedule = JtxtSchedule.getText();
        String jornada = cmbJornada.getSelectedItem().toString();
        // Declare Setters
        SaveSubject.setSubjectName(SubjectName);
        SaveSubject.setSemester(semester);
        SaveSubject.setJornada(jornada);
        SaveSubject.setSchedule(schedule);

        // Save Student & increment counter

        subjectList.add(subjectCount, SaveSubject);
        subjectCount++;

        for (SubjectObject subject : subjectList) {
            cmbAsignSubject.addItem(subject.getSubjectName()+' '+subject.getSchedule());
        }

    }
