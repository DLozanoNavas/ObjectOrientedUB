    public void SaveStudent() {

        StudentObject SaveStudent = new StudentObject();
        // Getters declaration
        long code = Long.parseLong(JtxtCode.getText());
        long phone = Long.parseLong(JtxtPhone.getText());
        long identification = Long.parseLong(JtxtIdentification.getText());
        String adress = JtxtAdress.getText();
        String name = JtxtName.getText();
        String lastName = JtxtLastName.getText();
        String mail = JtxtMail.getText();
        char gender = '-';
        if(rbtnFemale.isSelected()){
            gender = 'F';
        }else{
            gender = 'M';
        }
        
        // Setters declaration
        SaveStudent.setIdentification(identification);
        SaveStudent.setName(name);
        SaveStudent.setLastName(lastName);
        SaveStudent.setCode(code);
        SaveStudent.setPhone(phone);
        SaveStudent.setAdress(adress);
        SaveStudent.setMail(mail);
        SaveStudent.setGender(gender);
        // Save Student & increment counter
        boolean exists = false;
        for (StudentObject student : studentList) {
            if (student.getCode() == code || student.getIdentification() == identification) {
                exists = true;
            } else {
                exists = false;
            }
        }
        if (exists == false) {
            studentList.add(studentCount, SaveStudent);
            studentCount++;
            for (StudentObject student : studentList) {
                cmbAsignStudent.addItem(student.getName()+' '+student.getLastName());
            }
            
        } else {
            JOptionPane.showConfirmDialog(null, "El estudiante ya existe.");
        }
        
    }
