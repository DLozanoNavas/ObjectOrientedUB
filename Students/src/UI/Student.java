    public void dataInit() {   
        try{
        String cant = JOptionPane.showInputDialog(this, "Digite el numero de estudiantes");
        int cantInt = Integer.parseInt(cant);

        if (cant == ""){
           System.exit(0);
        }else{
            if (cantInt < 2) {
                JOptionPane.showMessageDialog(null, "El numero de estudiantes debe ser mayor o igual a 2");
                dataInit();
            }else{
                
            studentsList = new StudentObject[cantInt];
            }
        }
        String cantAsignInput = JOptionPane.showInputDialog(this, "Digite el numero de asignaturas");
        int cantAsign = Integer.parseInt(cantAsignInput);
 
        if (cantAsignInput == null){
            dataInit();
        }
        if (cantAsign < 2){
            JOptionPane.showMessageDialog(null, "El numero de asignaturas debe ser mayor o igual a 2");
            dataInit();
        }
        else{
           subjectsList = new SubjectObject[Integer.parseInt(cantAsignInput)];
        }
        
        for (int i = 0; i <= studentsList.length; i++) {
                SaveStudent();
        }
        btnSaveStudent.setEnabled(true);
        }catch(HeadlessException | NumberFormatException NumberFormatException){
            System.out.println("Exception catched!");
            int cantInt = Integer.parseInt(cant );
            do{
            String cant = JOptionPane.showInputDialog(this, "Digite el numero de estudiantes");
            
            }while(cantInt < 2 );
        }
}
