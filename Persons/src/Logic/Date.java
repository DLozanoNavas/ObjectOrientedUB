package Logic;


public class Date {

    private int day;

    private int month;

    private int year;

    public Date() {
    }

    public int getYear () {
        return year;
    }

    public void setYear (int val) {
        this.year = val;
    }

    public int getDay () {
        return day;
    }

    public void setDay (int val) {
        this.day = val;
    }

    public int getMonth () {
        return month;
    }

    public void setMonth (int val) {
        this.month = val;
    }

}

